//If else + if else

let nota = 5;

if (nota === 5){
    console.log("Felicidades, obtuviste un sobresaliente");
} else if (nota === 4) {
    console.log("Buen trabajo, pero podraia haberlo hecho mucho mejor")
}else if (nota === 3) {
    console.log("Has obetnido un suficiente")
}else if (nota === 2) {
    console.log("No has aprobado por poco")
}else if (nota === 1) {
    console.log("Mejora")
}else {
    console.log("Error, introduce una nota entre 1 y 5")
}